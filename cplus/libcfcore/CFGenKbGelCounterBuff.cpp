	void CFGenKbGelCounterBuff::setRequiredCounterName( const std::string& value ) {
		static const std::string S_ProcName( "setRequiredCounterName" );
		if( value.length() > CFGenKbGelCounterBuff::COUNTERNAME_MAX_LEN ) {
			throw cflib::CFLibArgumentOverflowException( CLASS_NAME,
				S_ProcName,
				1,
				S_VALUE_LENGTH,
				value.length(),
				CFGenKbGelCounterBuff::COUNTERNAME_MAX_LEN );
		}
		if( requiredCounterName != NULL ) {
			delete requiredCounterName;
			requiredCounterName = NULL;
		}
		requiredCounterName = new std::string( value );
	}

